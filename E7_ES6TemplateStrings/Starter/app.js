// Template Literal : A way to concatenate strings in javascript (ES6)

//   `   <--- that thing is a 'backtick' or 'grave accent'

var name = 'John Doe';

var greet = 'Hello ' + name;

var greet2 = `Hello ${ name }`; // this is a template Literal

console.log(greet);

console.log(greet2);